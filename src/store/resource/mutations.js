export const mutations = {
  SET_REGIONS: (state, regions) => {
    state.regions = regions
  },
  SET_CITIES: (state, cities) => {
    state.cities = cities
  },
  SET_DISEASES: (state, diseases) => {
    state.diseases = diseases
  },
  SET_USERS: (state, users) => {
    state.users = users
  }
}
